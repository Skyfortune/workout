package madcow;

public class DTO {

	private String id;
	private int squat;
	private int bench_press;
	private int military_press;
	private int deadlift;
	private int barbell_row;
	private int turm;

	
	public int getSquat() {
		return squat;
	}

	public void setSquat(int squat) {
		this.squat = squat;
	}

	public int getBench_press() {
		return bench_press;
	}

	public void setBench_press(int bench_press) {
		this.bench_press = bench_press;
	}

	public int getMilitary_press() {
		return military_press;
	}

	public void setMilitary_press(int military_press) {
		this.military_press = military_press;
	}

	public int getBarbell_row() {
		return barbell_row;
	}

	public void setBarbell_row(int barbell_row) {
		this.barbell_row = barbell_row;
	}

	public int getDeadlift() {
		return deadlift;
	}

	public void setDeadlift(int deadlift) {
		this.deadlift = deadlift;
	}

	public int getTurm() {
		return turm;
	}
 
	public void setTurm(int turm) {
		this.turm = turm;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

}
